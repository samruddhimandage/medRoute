const DICTS: Record<LangCode, Dict> = { en, hi, mr, ta, bn, te };

const STORAGE_KEY = "medroute:lang";

function load(): LangCode {
  if (typeof window === "undefined") return "en";
  const v = localStorage.getItem(STORAGE_KEY);
  if (v && DICTS[v as LangCode]) return v as LangCode;
  return "en";
}

let current: LangCode = load();
const listeners = new Set<() => void>();

export const i18n = {
  get: () => current,
  set(code: LangCode) {
    current = code;
    if (typeof window !== "undefined") localStorage.setItem(STORAGE_KEY, code);
    listeners.forEach((l) => l());
  },
  subscribe(l: () => void) {
    listeners.add(l);
    return () => listeners.delete(l);
  },
};

export function useLang(): LangCode {
  return useSyncExternalStore(
    i18n.subscribe,
    () => current,
    () => "en" as LangCode
  );
}

export function useT() {
  const lang = useLang();
  return (key: keyof typeof en | string, vars?: Record<string, string | number>) => {
    const dict = DICTS[lang] ?? en;
    let v = dict[key as string] ?? en[key as string] ?? (key as string);
    if (vars) {
      for (const [k, val] of Object.entries(vars)) {
        v = v.replace(new RegExp(`\\{${k}\\}`, "g"), String(val));
      }
    }
    return v;
  };
}

